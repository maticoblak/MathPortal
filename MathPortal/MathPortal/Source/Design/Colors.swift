import UIKit

extension UIColor {
    
    class var mathOrange: UIColor {
        return UIColor(red: 255/255, green: 113/255, blue: 89/255, alpha: 1)
    }
    
    class var mathPink: UIColor {
        return UIColor(red: 211/255, green: 48/255, blue: 75/255, alpha: 1)
    }
    
    class var mathDarkBlue: UIColor {
        return UIColor(red: 1/255, green: 14/255, blue: 43/255, alpha: 1)
    }
    
    class var mathLightGrey: UIColor {
        return UIColor(red: 113/255, green: 123/255, blue: 151/255, alpha: 1)
    }
    
    class var mathDarkGrey: UIColor {
        return UIColor(red: 30/255, green: 49/255, blue: 84/255, alpha: 1)
    }

    
}
